     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request->all());
        $prayer = New Prayer;
        $prayer->fullname = Auth::user()->name;
        $prayer->email = Auth::user()->email;
        $prayer->phone = $request->phone;
        $prayer->title = $request->title;
        $prayer->prayer_request = $request->prayer_request;
        $prayer->request_response = $request->request_response;
        $prayer->save();
        
        return redirect()->back()->with('message', 'Thank you for sending.');
    }

